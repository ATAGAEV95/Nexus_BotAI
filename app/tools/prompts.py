from datetime import datetime


def get_steam_free_games_prompt() -> tuple[str, str]:
    """Возвращает (system_prompt, user_prompt) для поиска бесплатных игр в Steam."""
    now = datetime.now().strftime("%d.%m.%Y")
    # now = "12.02.2026"  # ДЛЯ ТЕСТОВ: Раскомментируй эту строку и впиши нужную дату для проверки прошлых раздач
    
    system_prompt = (
        "Ты — робот, работающий по строгому протоколу. "
        "Твоя задача: проверить наличие 100% скидок на платные игры в Steam. "
        "ПРАВИЛА ВЫВОДА:\n"
        "1. Если игры ЕСТЬ, выведи строго список:\n"
        "Игры, которые сегодня раздают бесплатно в Steam:\n"
        "[Название] - [Жанр]\n"
        "[Название] - [Жанр]\n"
        "2. Если игр НЕТ (или только мусор/f2p), выведи ТОЛЬКО одну фразу и ЗАМОЛЧИ:\n"
        "Сегодня раздач игр в Steam нет.\n\n"
        "ЗАПРЕЩЕНО: писать вступления, объяснения ('поиск не дал результатов', 'найдены только...'), извинения. "
        "Любой лишний текст — это ошибка."
    )

    user_prompt = (
        f"Проверь Steam на наличие 100% скидок на платные игры именно {now}. "
        "Игнорируй: Free-to-Play, демо, DLC, раздачи на других сайтах. "
        "Если ничего стоящего нет — выведи фразу об отсутствии и ничего больше."
    )
    
    return system_prompt, user_prompt
